#include "Field.h"

namespace PDCommon::Field {

// ---------------------------------------------------------------------------
// 构造函数：初始化场名称与分量维度
// ---------------------------------------------------------------------------
Field::Field(const std::string &name, int dim)
    : name_(name), dim_(dim) {}

// ---------------------------------------------------------------------------
// 属性访问器
// ---------------------------------------------------------------------------
const std::string &Field::getName() const { return name_; }

int Field::getDim() const { return dim_; }

} // namespace PDCommon::Field
